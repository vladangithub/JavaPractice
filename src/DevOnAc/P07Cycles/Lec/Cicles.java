package DevOnAc.P07Cycles.Lec;

public class Cicles {
    public static void main(String[] args) {
        System.out.println("hello, world");
        System.out.println("hello, world");
        System.out.println("hello, world");
        System.out.println("hello, world");
        System.out.println("hello, world");
        System.out.println("hello, world");
        System.out.println("hello, world");
        System.out.println("hello, world");
        System.out.println("hello, world");
        System.out.println("hello, world");
        System.out.println();
        for (int i = 0; i <= 5; i++) {
            System.out.println("hello, world");
        }
    }
}
